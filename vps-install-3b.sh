echo -e "  Vhost:   $VHOST_FILE"
echo ""
echo -e "${CYAN}Next steps for this tenant:${NC}"
echo "  1. Point DNS: ${DOMAIN} → 72.62.160.72 (A record)"
echo "  2. Get cert:  certbot certonly --webroot -w /var/www/html -d ${DOMAIN}"
echo "     (or)       certbot certonly --standalone --preferred-challenges http -d ${DOMAIN}"
