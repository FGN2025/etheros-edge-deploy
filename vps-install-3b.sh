#!/usr/bin/env bash
set -euo pipefail

EDGE_DIR="/opt/etheros-edge"
ISP_CONFIG_DIR="$EDGE_DIR/isp-config"
NGINX_CONF_DIR="$EDGE_DIR/nginx/conf.d"
TEMPLATE="$EDGE_DIR/nginx/tenant-vhost.conf.template"

GREEN='\033[0;32m'; CYAN='\033[0;36m'; YELLOW='\033[1;33m'; BOLD='\033[1m'; RED='\033[0;31m'; NC='\033[0m'

usage() {
    echo -e "${BOLD}Usage:${NC} $0 --slug <slug> --name <name> --domain <domain> [--accent <hex>]"
    echo ""
    echo "  --slug    Lowercase slug (letters, numbers, hyphens)  e.g. valley-fiber"
    echo "  --name    Display name                                e.g. \"Valley Fiber\""
    echo "  --domain  Full domain for this ISP's portal           e.g. edge.valleyfiber.com"
    echo "  --accent  Accent hex color (optional, default #00C2CB)"
    echo ""
    echo -e "${BOLD}Example:${NC}"
    echo "  $0 --slug valley-fiber --name \"Valley Fiber\" --domain edge.valleyfiber.com"
    exit 1
}

SLUG=""; NAME=""; DOMAIN=""; ACCENT="#00C2CB"
while [[ $# -gt 0 ]]; do
    case "$1" in
        --slug)   SLUG="$2";   shift 2 ;;
        --name)   NAME="$2";   shift 2 ;;
        --domain) DOMAIN="$2"; shift 2 ;;
        --accent) ACCENT="$2"; shift 2 ;;
        -h|--help) usage ;;
        *) echo -e "${RED}Unknown argument: $1${NC}"; usage ;;
    esac
done

[[ -z "$SLUG" || -z "$NAME" || -z "$DOMAIN" ]] && { echo -e "${RED}Error: --slug, --name, --domain required${NC}"; echo ""; usage; }
[[ ! "$SLUG" =~ ^[a-z0-9-]+$ ]] && { echo -e "${RED}Error: slug must be lowercase letters/numbers/hyphens only${NC}"; exit 1; }

echo -e "${CYAN}${BOLD}━━━ Adding ISP Tenant ━━━${NC}"
echo -e "  Slug:    ${SLUG}"
echo -e "  Name:    ${NAME}"
echo -e "  Domain:  ${DOMAIN}"
echo -e "  Accent:  ${ACCENT}"
echo ""

mkdir -p "$ISP_CONFIG_DIR" "$NGINX_CONF_DIR"

# Write JSON config
CONFIG_FILE="$ISP_CONFIG_DIR/${SLUG}.json"
echo -e "${YELLOW}▸${NC} Writing config → $CONFIG_FILE"
cat > "$CONFIG_FILE" << JSONEOF
{
  "slug": "${SLUG}",
  "name": "${NAME}",
  "domain": "${DOMAIN}",
  "logo_url": "",
  "accent_color": "${ACCENT}",
  "primary_color": "#0D1B2A",
  "support_email": "support@${DOMAIN#edge.}",
  "plan_personal_price": 49,
  "plan_professional_price": 99,
  "max_terminals": 500
}
JSONEOF
echo -e "  ${GREEN}✓${NC} Config saved"

# Generate nginx vhost from template
if [[ ! -f "$TEMPLATE" ]]; then
    echo -e "${RED}Error: template not found at $TEMPLATE${NC}"
    exit 1
fi

VHOST_FILE="$NGINX_CONF_DIR/${SLUG}.conf"
echo -e "${YELLOW}▸${NC} Generating nginx vhost → $VHOST_FILE"
sed \
    -e "s|__DOMAIN__|${DOMAIN}|g" \
    -e "s|__SLUG__|${SLUG}|g" \
    -e "s|__ISP_NAME__|${NAME}|g" \
    -e "s|__ACCENT_COLOR__|${ACCENT}|g" \
    "$TEMPLATE" > "$VHOST_FILE"
echo -e "  ${GREEN}✓${NC} Vhost generated"

# Test nginx config
