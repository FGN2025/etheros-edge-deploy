printf "%-20s %-25s %-35s %-10s\n" "────────────────────" "─────────────────────────" "───────────────────────────────────" "──────────"

shopt -s nullglob
configs=("$ISP_CONFIG_DIR"/*.json)
if [[ ${#configs[@]} -eq 0 ]]; then
    echo -e "${YELLOW}No ISP tenants configured yet.${NC}"
    echo "Run: ./add-isp-tenant.sh --slug myisp --name \"My ISP\" --domain edge.myisp.com"
    exit 0
