#!/usr/bin/env python3
"""Patch docker-compose.yml to set WEBUI_ISP_TENANT env var on the open-webui service."""
import argparse, shutil, sys, yaml
from pathlib import Path

COMPOSE = Path("/opt/etheros-edge/docker-compose.yml")

parser = argparse.ArgumentParser(description="Set WEBUI_ISP_TENANT in docker-compose.yml")
parser.add_argument("--slug", required=True, help="ISP tenant slug")
args = parser.parse_args()

# Backup
backup = COMPOSE.with_suffix(".yml.bak")
shutil.copy2(COMPOSE, backup)
print(f"Backup: {backup}")

data = yaml.safe_load(COMPOSE.read_text())
services = data.get("services", {})

# Find open-webui service
owui_key = next((k for k in services if "open-webui" in k or "openwebui" in k.lower()), None)
if not owui_key:
    print(f"ERROR: open-webui service not found. Services: {list(services.keys())}", file=sys.stderr)
    sys.exit(1)

svc = services[owui_key]
env = svc.get("environment", [])

# Convert list-style env to dict if needed
if isinstance(env, list):
    env_dict = {}
    for e in env:
        if "=" in e:
            k, v = e.split("=", 1)
            env_dict[k] = v
        else:
            env_dict[e] = None
    env = env_dict

env["WEBUI_ISP_TENANT"] = args.slug
svc["environment"] = env
print(f"Set WEBUI_ISP_TENANT={args.slug} on service '{owui_key}'")

COMPOSE.write_text(yaml.dump(data, default_flow_style=False, sort_keys=False))
print(f"Wrote {COMPOSE}")
print("Run: cd /opt/etheros-edge && docker compose up -d --no-deps --force-recreate open-webui")
PYEOF
