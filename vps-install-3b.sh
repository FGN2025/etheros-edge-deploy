# EtherOS Sprint 3B — ISP Branding Layer

## Overview
Each ISP gets their own branded portal at a custom domain (e.g. edge.valleyfiber.com).
The branding config drives: ISP name, accent color, logo URL, and domain routing.

## Quick Start: Add a New ISP Tenant

```bash
cd /opt/etheros-edge

# 1. Add the tenant (creates JSON config + nginx vhost)
./add-isp-tenant.sh --slug valley-fiber --name "Valley Fiber" --domain edge.valleyfiber.com

# 2. Point the ISP's DNS: edge.valleyfiber.com → 72.62.160.72

# 3. Get a TLS cert for the new domain
certbot certonly --standalone --preferred-challenges http -d edge.valleyfiber.com

# 4. Reload nginx to pick up the new cert
docker exec etheros-nginx nginx -s reload
```

## Config Schema (isp-config/{slug}.json)
| Field                  | Description                              |
|------------------------|------------------------------------------|
| slug                   | URL-safe identifier (e.g. valley-fiber)  |
| name                   | Display name shown in the UI             |
| domain                 | Full domain for this ISP's portal        |
| accent_color           | Hex color for teal accent (#00C2CB)      |
| primary_color          | Hex color for nav/header (#0D1B2A)       |
| logo_url               | URL to ISP logo image (optional)         |
| support_email          | Auto-populated from domain               |
| plan_personal_price    | $/yr for personal plan (default 49)      |
| plan_professional_price| $/yr for professional plan (default 99)  |
| max_terminals          | Terminal cap for this ISP (default 500)  |

## Other Commands
```bash
./list-isp-tenants.sh           # Show all configured tenants
./remove-isp-tenant.sh --slug valley-fiber   # Remove a tenant
python3 docker-compose-patch.py --slug valley-fiber  # Set active tenant in docker-compose
```

## How Nginx Vhost Injection Works
add-isp-tenant.sh reads nginx/tenant-vhost.conf.template and substitutes:
  __DOMAIN__      → actual domain
  __SLUG__        → tenant slug
  __ISP_NAME__    → display name
  __ACCENT_COLOR__ → hex accent color

The generated .conf is written to nginx/conf.d/{slug}.conf and nginx reloads automatically.
ISP identity headers (X-ISP-Tenant, X-ISP-Name, X-ISP-Accent) are injected on every request.
