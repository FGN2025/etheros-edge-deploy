    maxterm=$(python3 -c "import json,sys; d=json.load(open('$f')); print(d.get('max_terminals','?'))")
    printf "%-20s %-25s %-35s %-10s\n" "$slug" "$name" "$domain" "$maxterm"
done
echo ""
echo -e "Total: ${#configs[@]} tenant(s)"
SCRIPT
