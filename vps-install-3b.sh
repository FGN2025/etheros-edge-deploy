# EtherOS ISP Tenant vhost — __SLUG__
# Auto-generated by add-isp-tenant.sh — do not edit manually

upstream openwebui___SLUG__ {
    server etheros-open-webui:8080;
}

server {
    listen 443 ssl http2;
    server_name __DOMAIN__;

    ssl_certificate     /etc/letsencrypt/live/__DOMAIN__/fullchain.pem;
    ssl_certificate_key /etc/letsencrypt/live/__DOMAIN__/privkey.pem;
    ssl_protocols       TLSv1.2 TLSv1.3;
    ssl_ciphers         HIGH:!aNULL:!MD5;
    ssl_session_cache   shared:SSL:10m;

    client_max_body_size 100M;
    proxy_read_timeout   300s;

    add_header X-Frame-Options "SAMEORIGIN" always;
    add_header X-Content-Type-Options "nosniff" always;
    add_header Strict-Transport-Security "max-age=31536000; includeSubDomains" always;
    add_header X-ISP-Tenant "__SLUG__" always;
    add_header X-ISP-Name "__ISP_NAME__" always;
    add_header X-ISP-Accent "__ACCENT_COLOR__" always;

    location /api/ {
        proxy_pass         http://openwebui___SLUG__/api/;
        proxy_set_header   Host $host;
        proxy_set_header   X-Real-IP $remote_addr;
        proxy_set_header   X-Forwarded-For $proxy_add_x_forwarded_for;
        proxy_set_header   X-Forwarded-Proto $scheme;
        proxy_set_header   X-ISP-Tenant "__SLUG__";
        proxy_http_version 1.1;
        proxy_set_header   Upgrade $http_upgrade;
        proxy_set_header   Connection "upgrade";
    }

    location /ws/ {
        proxy_pass         http://openwebui___SLUG__/ws/;
