#!/usr/bin/env bash
set -euo pipefail
EDGE_DIR="/opt/etheros-edge"
ISP_CONFIG_DIR="$EDGE_DIR/isp-config"
CYAN='\033[0;36m'; BOLD='\033[1m'; NC='\033[0m'; YELLOW='\033[1;33m'

